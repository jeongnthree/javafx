package ch01_variable_operator;

public class Mybox {
    public static void main(String[] args) {
        int 반지름;
        double 원주율;
        double 넓이;

        반지름 = 10;
        원주율 = 3.14;
        넓이 = 반지름*반지름*원주율;

        System.out.println("반지름 :" + 반지름);
        System.out.println("원주율 :" + 원주율);
        System.out.println("넓이 :" + 넓이);


    }
}
